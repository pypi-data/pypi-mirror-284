


import os
import typing

import jk_prettyprintobj

from .DirEntryX import DirEntryX






class EmitFilter(jk_prettyprintobj.DumpMixin):

	################################################################################################################################
	## Constructor
	################################################################################################################################

	def __init__(self,
			emitWalkRoot:bool = False,
			emitErrors:bool = True,
		) -> None:

		assert isinstance(emitWalkRoot, bool)
		assert isinstance(emitErrors, bool)

		self.emitWalkRoot = emitWalkRoot
		self.emitErrors = emitErrors
	#

	################################################################################################################################
	## Public Properties
	################################################################################################################################

	################################################################################################################################
	## Helper Methods
	################################################################################################################################

	def _dumpVarNames(self) -> list:
		return [
			"emitWalkRoot",
			"emitErrors",
		]
	#

	################################################################################################################################
	## Public Methods
	################################################################################################################################

	def checkEmit(self, entry:DirEntryX) -> bool:
		return True
	#

